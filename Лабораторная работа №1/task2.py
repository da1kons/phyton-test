# TODO Найдите количество книг, которое можно разместить на дискете

# Данные задачи
disk_size_mb = 1.44  # объем дискеты в Мб
pages = 100  # количество страниц в книге
lines_per_page = 50  # число строк на странице
chars_per_line = 25  # количество символов в строке
bytes_per_char = 4  # количество байт на один символ

# Рассчитываем объем одной книги
total_chars = pages * lines_per_page * chars_per_line  # общее количество символов в книге
book_size_bytes = total_chars * bytes_per_char  # объем одной книги в байтах

# Объем дискеты в байтах
disk_size_bytes = disk_size_mb * 1024 * 1024  # переводим Мб в байты

# Рассчитываем количество книг, которые можно поместить на дискету
num_books = disk_size_bytes // book_size_bytes  # целое число книг

# Вывод результата
print(f"Количество книг, помещающихся на дискету: {int(num_books)}")
